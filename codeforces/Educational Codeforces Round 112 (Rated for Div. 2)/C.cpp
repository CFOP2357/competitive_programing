void solve(){
    cin>>m;
    vector<vector<ll>> sum(2, vector<ll>(m+1, 0));
    for(int i=0; i<2; i++){
        for(int j=1; j<=m; j++){
            ll z; cin>>z;
            sum[i][j] = sum[i][j-1]+z;
        }
    }

    ll ans = LLONG_MAX;
    for(int i=1; i<=m; i++){
        ll bob = max(sum[1][i-1], sum[0][m]-sum[0][i]);
        ans = min(ans, bob);
    }

    cout<<ans<<"\n";
}


