void solve(){
    cin>>n>>m;

    int current_column, current_row;
    int goal_column, goal_row;

    cin>>current_row>>current_column>>goal_row>>goal_column;


    int r = 1, c = 1;

    int ans = 0;
    while(true){
      if(current_row == goal_row or current_column == goal_column){
        cout<<ans<<"\n";
        return;
      }

      if(current_row + r < 1 or current_row + r > n)
            r *= -1;
      if(current_column + c < 1 or current_column + c > m)
            c *= -1;

      current_row += r;
      current_column += c;

      ans++;
    }

}


