string s;

void solve(){
    cin>>s;

    map<char, int> f;
    for(auto c : s)
        f[c]++;

    for(auto [c, k] : f)
        if(k > s.size()/2){
            cout<<"IMPOSSIBLE\n";
            return;
        }

    string ans = s;
    for(int i=0; i<s.size(); i++){
        for(int j=0; j<s.size(); j++){
            if(s[i] != ans[j] and s[j]!=ans[i]){
                swap(ans[i], ans[j]);
                break;
            }
        }
    }
    cout<<ans<<"\n";

}


